#!/usr/bin/env node
/**
 * Generate backlink-preservation redirects from content/blog/*.md slugs.
 *
 * Produces:
 *   - vercel.json (kubesimplify.com)
 *     * rewrites: blog.kubesimplify.com/<slug> → /blog/<slug>  (URL stays)
 *     * redirects: kubesimplify.com/<slug>      → /blog/<slug>  (301, server-side)
 *     * redirects: kubesimplify.com/blogs       → /blog
 *
 *   - public/_redirects (Cloudflare Pages, blog.kubesimplify.com)
 *     * 301 cleanup: /blog/<slug>               → /<slug>
 *     * rewrite:     /<slug>                    → /blog/<slug>  (URL stays)
 *
 * Why explicit per-slug entries instead of a regex?
 *   Regex catch-alls eat new top-level pages we add later. A frozen list of
 *   the 178 known blog slugs is bulletproof. Anything not in the list 404s
 *   normally (and the client-side not-found.js still tries /blog/<slug> as a
 *   last resort for slugs we miss).
 */
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const CONTENT = join(ROOT, 'content', 'blog');

function parseFm(raw) {
  const m = raw.match(/^---\n([\s\S]*?)\n---/);
  if (!m) return {};
  const fm = {};
  for (const line of m[1].split('\n')) {
    const mm = line.match(/^([a-zA-Z_]+):\s*(.*)$/);
    if (!mm) continue;
    let [, k, v] = mm;
    v = v.trim();
    if (v.startsWith('"') && v.endsWith('"')) v = v.slice(1, -1);
    fm[k] = v;
  }
  return fm;
}

const files = readdirSync(CONTENT).filter((f) => f.endsWith('.md'));
const slugs = files
  .map((f) => parseFm(readFileSync(join(CONTENT, f), 'utf8')).slug || f.replace(/\.md$/, ''))
  .filter(Boolean)
  .sort();

console.log(`Found ${slugs.length} slugs.`);

// ── vercel.json (kubesimplify.com) ───────────────────────────────────────
//
// Strategy: kubesimplify.com hosts ONLY the main site (about, workshops, etc.).
// All blog URLs (/blog, /blog/*, /<old-slug>, /blogs) 301 off-domain to
// blog.kubesimplify.com so Google sees exactly one canonical URL per post.
// Eliminates duplicate-content concerns; consolidates link equity on the
// blog subdomain (where every backlink already points).
const BLOG = 'https://blog.kubesimplify.com';
const vercelConfig = {
  $schema: 'https://openapi.vercel.sh/vercel.json',
  redirects: [
    // Blog index → blog subdomain
    { source: '/blog', destination: BLOG + '/', permanent: true },
    { source: '/blogs', destination: BLOG + '/', permanent: true },
    // Blog meta routes
    { source: '/blog/authors', destination: BLOG + '/authors', permanent: true },
    { source: '/blog/write', destination: BLOG + '/write', permanent: true },
    { source: '/blog/series', destination: BLOG + '/series', permanent: true },
    { source: '/blog/series/:slug*', destination: BLOG + '/series/:slug*', permanent: true },
    { source: '/blog/tag/:slug*', destination: BLOG + '/tag/:slug*', permanent: true },
    { source: '/blog/author/:slug*', destination: BLOG + '/author/:slug*', permanent: true },
    { source: '/blog/hub/:slug*', destination: BLOG + '/hub/:slug*', permanent: true },
    { source: '/blog/page/:slug*', destination: BLOG + '/page/:slug*', permanent: true },
    // Each known post slug → blog subdomain
    ...slugs.map((slug) => ({
      source: `/blog/${slug}`,
      destination: `${BLOG}/${slug}`,
      permanent: true,
    })),
    // Legacy pre-Hashnode root-level slugs → blog subdomain
    ...slugs.map((slug) => ({
      source: `/${slug}`,
      destination: `${BLOG}/${slug}`,
      permanent: true,
      has: [{ type: 'host', value: 'kubesimplify.com' }],
    })),
  ],
};

writeFileSync(join(ROOT, 'vercel.json'), JSON.stringify(vercelConfig, null, 2) + '\n');

// ── public/_redirects (Cloudflare Pages, blog.kubesimplify.com) ──────────
//
// Rule order: 301s before 200s. First match wins per Cloudflare docs.
//   301! = permanent redirect (browser follows, URL changes), forced even
//          if a static file matches the source.
//   200  = rewrite (URL stays, server serves content from destination).
//
const lines = [
  '# Auto-generated by scripts/generate-redirects.mjs. Do not edit by hand.',
  '# Used by Cloudflare Pages when serving blog.kubesimplify.com',
  '',
  '# ── 1. Strip /blog/ prefix on direct hits (clean URLs) ───────────────',
  '/blog                  /                                        301!',
  '/blog/                 /                                        301!',
  '/blogs                 /                                        301!',
  '/blog/authors          /authors                                 301!',
  '/blog/write            /write                                   301!',
  '/blog/page/*           /page/:splat                             301!',
  '/blog/series           /series                                  301!',
  '/blog/series/*         /series/:splat                           301!',
  '/blog/tag/*            /tag/:splat                              301!',
  '/blog/author/*         /author/:splat                           301!',
  '/blog/hub/*            /hub/:splat                              301!',
  ...slugs.map((slug) => `/blog/${slug.padEnd(80, ' ')} /${slug}  301!`),
  '',
  '# ── 2. Redirect main-site routes off the blog subdomain.',
  '#       Files exist in the static output (one shared build), but they',
  '#       belong at kubesimplify.com, not blog.kubesimplify.com.',
  '/about                 https://kubesimplify.com/about           301!',
  '/workshops             https://kubesimplify.com/workshops       301!',
  '/partnerships          https://kubesimplify.com/partnerships    301!',
  '/resources             https://kubesimplify.com/resources       301!',
  '',
  '# ── 3. Serve blog content under clean URLs (URL stays as-is).',
  '#       `200!` forces the rewrite even when a static file would win',
  '#       (out/index.html is the main-site home; we need /blog content).',
  '/                      /blog                                    200!',
  '/authors               /blog/authors                            200!',
  '/write                 /blog/write                              200!',
  '/page/*                /blog/page/:splat                        200!',
  '/series                /blog/series                             200!',
  '/series/*              /blog/series/:splat                      200!',
  '/tag/*                 /blog/tag/:splat                         200!',
  '/author/*              /blog/author/:splat                      200!',
  '/hub/*                 /blog/hub/:splat                         200!',
  ...slugs.map((slug) => `/${slug.padEnd(80, ' ')} /blog/${slug}  200!`),
  '',
  '# Everything else (assets, sitemap.xml, rss.xml, robots.txt, llms.txt,',
  '# /img/, /_next/) is served directly by Cloudflare from the static',
  '# output. No rule needed.',
  '',
];

writeFileSync(join(ROOT, 'public', '_redirects'), lines.join('\n'));

// ── public/_worker.js (Cloudflare Pages Advanced Mode) ────────────────────
// We had trouble with _redirects on Pages. _worker.js is JS-based routing
// at the edge — same logic but guaranteed to fire.
const workerJs = `// AUTO-GENERATED by scripts/generate-redirects.mjs. Do not edit by hand.
// Cloudflare Pages Advanced Mode: handles all routing for the blog deployment.
// Replaces _redirects (Pages picks _worker.js over _redirects when both exist).

const KUBESIMPLIFY_ROUTES = new Set(['/about', '/workshops', '/partnerships', '/resources']);
const BLOG_SLUGS = new Set(${JSON.stringify(slugs)});

export default {
  async fetch(request, env) {
    const url = new URL(request.url);
    const path = url.pathname.replace(/\\/$/, '') || '/';
    const search = url.search;

    // 0. Normalize .html suffixes to clean URLs (Pagefind result URLs and
    //    old direct links point at the raw static files, which only exist
    //    under /blog/<slug>.html and would otherwise 404 after the prefix
    //    strip below).
    if (path.endsWith('.html')) {
      const clean = path.slice(0, -5).replace(/\\/index$/, '') || '/';
      return Response.redirect(url.origin + clean + search, 301);
    }

    // 1. Strip /blog/ prefix FIRST — before asset detection. Otherwise
    //    /blog/rss.xml looks like an asset, gets a 404 (no file at that path),
    //    and the Next.js 404 page's JS loops back to /blog/rss.xml.
    if (path === '/blog') {
      return Response.redirect(url.origin + '/' + search, 301);
    }
    if (path.startsWith('/blog/')) {
      const rest = path.slice(5); // remove '/blog'
      return Response.redirect(url.origin + rest + search, 301);
    }

    // 2. Assets: serve as-is, no rewrite
    if (
      path.startsWith('/_next/') ||
      path.startsWith('/img/') ||
      path === '/sitemap.xml' ||
      path === '/rss.xml' ||
      path === '/atom.xml' ||
      path === '/robots.txt' ||
      path === '/llms.txt' ||
      path === '/favicon.ico' ||
      /\\.[a-zA-Z0-9]+$/.test(path)
    ) {
      return env.ASSETS.fetch(request);
    }

    // 3. Main-site routes don't belong on the blog subdomain
    if (KUBESIMPLIFY_ROUTES.has(path)) {
      return Response.redirect('https://kubesimplify.com' + path + search, 301);
    }

    // 4. Legacy index → /
    if (path === '/blogs') {
      return Response.redirect(url.origin + '/' + search, 301);
    }

    // / → serve /blog content (URL stays /)
    if (path === '/') {
      return env.ASSETS.fetch(new Request(url.origin + '/blog' + search, request));
    }

    // Known blog sub-routes: rewrite to /blog/<...>
    const slugCandidate = path.slice(1); // strip leading /
    if (
      BLOG_SLUGS.has(slugCandidate) ||
      path === '/authors' ||
      path === '/write' ||
      path.startsWith('/authors/') ||
      path.startsWith('/tag/') ||
      path.startsWith('/author/') ||
      path.startsWith('/hub/') ||
      path.startsWith('/page/') ||
      path === '/series' ||
      path.startsWith('/series/')
    ) {
      return env.ASSETS.fetch(new Request(url.origin + '/blog' + path + search, request));
    }

    // Anything else: try static lookup (Pages will 404 if missing)
    return env.ASSETS.fetch(request);
  }
};
`;
writeFileSync(join(ROOT, 'public', '_worker.js'), workerJs);

// ── public/_routes.json ───────────────────────────────────────────────────
// Tell Pages which paths to send through the Worker. Assets bypass entirely
// (no Workers-request quota burned for /_next/* /img/* etc.).
const routesJson = {
  version: 1,
  include: ['/*'],
  exclude: ['/_next/*', '/img/*', '/favicon.ico', '/sitemap.xml', '/rss.xml', '/atom.xml', '/robots.txt', '/llms.txt'],
};
writeFileSync(join(ROOT, 'public', '_routes.json'), JSON.stringify(routesJson, null, 2));

console.log(`vercel.json:        ${vercelConfig.redirects.length} redirects`);
console.log(`public/_redirects:  ${lines.length} lines (fallback)`);
console.log(`public/_worker.js:  ${workerJs.split('\n').length} lines`);
console.log(`public/_routes.json: ${JSON.stringify(routesJson).length} bytes`);
